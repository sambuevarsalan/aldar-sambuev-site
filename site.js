const translatedElements = [...document.querySelectorAll("[data-i18n]")];
const originalContent = new Map(translatedElements.map((element) => [element, element.innerHTML]));
const languageButtons = [...document.querySelectorAll("[data-lang]")];
const description = document.querySelector('meta[name="description"]');

function applyLanguage(language) {
  const normalized = language === "en" ? "en" : "ru";
  document.documentElement.lang = normalized;

  translatedElements.forEach((element) => {
    const key = element.dataset.i18n;
    element.innerHTML = normalized === "en"
      ? (translations.en[key] || originalContent.get(element))
      : originalContent.get(element);
  });

  languageButtons.forEach((button) => {
    const active = button.dataset.lang === normalized;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", String(active));
  });

  document.title = normalized === "en"
    ? "Aldar Sambuev — junior figure skating"
    : "Алдар Самбуев — юниорское фигурное катание";

  if (description) {
    description.content = normalized === "en"
      ? "Aldar Sambuev — junior figure skater. Programs, performances, sporting journey, and collaboration."
      : "Алдар Самбуев — юниорский фигурист, КМС. Программы, выступления, спортивный путь и сотрудничество.";
  }

  localStorage.setItem("aldar-site-language", normalized);
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

applyLanguage(localStorage.getItem("aldar-site-language") || "ru");

const header = document.querySelector(".site-header");

function updateHeader() {
  header?.classList.toggle("is-scrolled", window.scrollY > 24);
}

updateHeader();
window.addEventListener("scroll", updateHeader, { passive: true });

const revealElements = [...document.querySelectorAll("[data-reveal]")];

if ("IntersectionObserver" in window && !window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
  document.documentElement.classList.add("reveal-ready");
  const revealObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      entry.target.classList.add("is-visible");
      observer.unobserve(entry.target);
    });
  }, { rootMargin: "0px 0px -8%", threshold: 0.08 });

  revealElements.forEach((element) => revealObserver.observe(element));
} else {
  revealElements.forEach((element) => element.classList.add("is-visible"));
}

const contactForm = document.querySelector("#contact-form");
const formStatus = document.querySelector("#form-status");

if (contactForm) {
  contactForm.addEventListener("submit", (event) => {
    event.preventDefault();

    const formData = new FormData(contactForm);
    const language = document.documentElement.lang;
    const typeSelect = contactForm.elements.type;
    const typeLabel = typeSelect.options[typeSelect.selectedIndex]?.textContent || "";
    const subject = language === "en"
      ? `Project proposal for Aldar Sambuev — ${typeLabel}`
      : `Предложение для Алдара Самбуева — ${typeLabel}`;
    const lines = language === "en"
      ? [
          `Name / organization: ${formData.get("name")}`,
          `Contact: ${formData.get("contact")}`,
          `Proposal type: ${typeLabel}`,
          "",
          String(formData.get("message"))
        ]
      : [
          `Имя / организация: ${formData.get("name")}`,
          `Контакт: ${formData.get("contact")}`,
          `Тип предложения: ${typeLabel}`,
          "",
          String(formData.get("message"))
        ];

    if (formStatus) {
      formStatus.textContent = language === "en"
        ? "Opening your email application…"
        : "Открываем ваше почтовое приложение…";
    }

    window.location.href = `mailto:contact@aldarsambuev.ru?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(lines.join("\n"))}`;
  });
}
